/**
 * This OpMode illustrates the basics of using the VuforiaOld engine to determine
 * the identity of VuforiaOld VuMarks encountered on the field. The code is structured as
 * a LinearOpMode. It shares much structure with [ConceptVuforiaNavigation]; we do not here
 * duplicate the core VuforiaOld documentation found there, but rather instead focus on the
 * differences between the use of VuforiaOld for navigation vs VuMark identification.
 *
 * @see ConceptVuforiaNavigation
 *
 * @see VuforiaLocalizer
 *
 * @see VuforiaTrackableDefaultListener
 * see  ftc_app/doc/tutorial/FTC_FieldCoordinateSystemDefinition.pdf
 *
 * Use Android Studio to Copy this Class, and Paste it into your team's code folder with a new name.
 * Remove or comment out the @Disabled line to add this opmode to the Driver Station OpMode list.
 *
 * IMPORTANT: In order to use this OpMode, you need to obtain your own VuforiaOld license key as
 * is explained in {@link ConceptVuforiaNavigation}.
 */

@Autonomous(name = "Vuforia Testing", group = "Concept")
class VuforiaTest : LinearOpMode() {

    internal var lastLocation: OpenGLMatrix? = null
    private val robot = Revbot()


    @Throws(InterruptedException::class)
    override fun runOpMode() {

        robot.init(hardwareMap)


        /*
         * To start up VuforiaOld, tell it the view that we wish to use for camera monitor (on the RC phone);
         * If no camera monitor is desired, use the parameterless constructor instead (commented out below).
         */
        val cameraMonitorViewId = hardwareMap.appContext.resources.getIdentifier("cameraMonitorViewId", "id", hardwareMap.appContext.packageName)
        val parameters = VuforiaLocalizer.Parameters(cameraMonitorViewId)

        // OR...  Do Not Activate the Camera Monitor View, to save power
        // VuforiaLocalizer.Parameters parameters = new VuforiaLocalizer.Parameters();

        /*
         * IMPORTANT: You need to obtain your own license key to use VuforiaOld. The string below with which
         * 'parameters.vuforiaLicenseKey' is initialized is for illustration only, and will not function.
         * A VuforiaOld 'Development' license key, can be obtained free of charge from the VuforiaOld developer
         * web site at https://developer.vuforia.com/license-manager.
         *
         * VuforiaOld license keys are always 380 characters long, and look as if they contain mostly
         * random data. As an example, here is a example of a fragment of a valid key:
         *      ... yIgIzTqZ4mWjk9wd3cZO9T1axEqzuhxoGlfOOI2dRzKS4T0hQ8kT ...
         * Once you've obtained a license key, copy the string from the VuforiaOld web site
         * and paste it in to your code onthe next line, between the double quotes.
         */
        parameters.vuforiaLicenseKey = Vuforia.VUFORIA_LICENSE_KEY

        /*
         * We also indicate which camera on the RC that we wish to use.
         * Here we chose the back (HiRes) camera (for greater range), but
         * for a competition robot, the front camera might be more convenient.
         */
        parameters.cameraDirection = VuforiaLocalizer.CameraDirection.BACK
        parameters.useExtendedTracking = false
        Vuforia.vuforia = ClassFactory.createVuforiaLocalizer(parameters)

        /*
         * Load the data set containing the VuMarks for Relic Recovery. There's only one trackable
         * in this data set: all three of the VuMarks in the game were created from this one template,
         * but differ in their instance id information.
         * @see VuMarkInstanceId
         */
        val relicTrackables = Vuforia.vuforia.loadTrackablesFromAsset("RelicVuMark")
        val relicTemplate = relicTrackables[0]
        relicTemplate.name = "relicVuMarkTemplate" // can help in debugging; otherwise not necessary

        telemetry.addData(">", "Press Play to start")
        telemetry.update()
        waitForStart()

        relicTrackables.activate()

        val STOP_RANGE = 50
        val TARGET_X = -1000
        val TARGET_Y = 0

        while (opModeIsActive()) {

            var str = ""
            /*
             * See if any of the instances of {@link relicTemplate} are currently visible.
             * {@link RelicRecoveryVuMark} is an enum which can have the following values:
             * UNKNOWN, LEFT, CENTER, and RIGHT. When a VuMark is visible, something other than
             * UNKNOWN will be returned by {@link RelicRecoveryVuMark#from(VuforiaTrackable)}.
             */
            val vuMark = RelicRecoveryVuMark.from(relicTemplate)
            if (vuMark != RelicRecoveryVuMark.UNKNOWN) {

                /* Found an instance of the template. In the actual game, you will probably
                 * loop until this condition occurs, then move on to act accordingly depending
                 * on which VuMark was visible. */
                telemetry.addData("VuMark", "%s visible", vuMark)

                /* For fun, we also exhibit the navigational pose. In the Relic Recovery game,
                 * it is perhaps unlikely that you will actually need to act on this pose information, but
                 * we illustrate it nevertheless, for completeness. */
                val pose = (relicTemplate.listener as VuforiaTrackableDefaultListener).pose
                telemetry.addData("Pose", format(pose))

                /* We further illustrate how to decompose the pose into useful rotational and
                 * translational components */
                if (pose != null) {
                    val trans = pose.translation
                    val rot = Orientation.getOrientation(pose, AxesReference.EXTRINSIC, AxesOrder.XYZ, AngleUnit.DEGREES)

                    // Extract the X, Y, and Z components of the offset of the target relative to the robot
                    val tX = trans.get(0).toDouble()
                    val tY = trans.get(1).toDouble()
                    val tZ = trans.get(2).toDouble()

                    // Extract the rotational components of the target relative to the robot
                    val rX = rot.firstAngle.toDouble()
                    val rY = rot.secondAngle.toDouble()
                    val rZ = rot.thirdAngle.toDouble()

                    if (tY >= TARGET_Y + STOP_RANGE) {
                        str += " LEFT "

                    } else if (tY <= TARGET_Y - STOP_RANGE) {
                        str += " RIGHT "
                    }

                    if (tZ >= TARGET_X + STOP_RANGE) {
                        str += " BACKWARDS "
                    } else if (tZ < TARGET_X - STOP_RANGE) {
                        str += " FORWARDS "
                    }

                    //telemetry.addData("x", tX);
                    //telemetry.addData("y", tY);
                    //telemetry.addData("z", tZ);

                    //robot.beep();

                }
            } else {
                //telemetry.addData("VuMark", "not visible");
                //commands.stopStrafing();
                //commands.stopDriving();
                str += " NOTFOUND "
            }

            if (str.isEmpty())
                str += "PERFECT!!!!!!!! :D"
            telemetry.clear()
            telemetry.addLine(str)

            telemetry.update()

        }
    }

    internal fun format(transformationMatrix: OpenGLMatrix?): String {
        return if (transformationMatrix != null) transformationMatrix.formatAsTransform() else "null"
    }

    companion object {

        val TAG = "VuforiaOld VuMark Sample"
    }
}
